    public void setStationNames(String[] stations) {
        cmbOrigin.removeAllItems();
        cmbDest.removeAllItems();
        for (String s : stations) {
            cmbOrigin.addItem(s);
            cmbDest.addItem(s);
        }
        this.pack();
        this.setLocationRelativeTo(null);
    }

