    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnUpdateActionPerformed
        if (!validateForm() || selectedRoute == null) {
            lblMessage.setText("Seleccione una ruta para actualizar.");
            return;
        }
        selectedRoute.setDistance(Double.parseDouble(txtDistance.getText().trim()));
        selectedRoute.setDepartureTime(txtDeparture.getText().trim());
        selectedRoute.setArrivalTime(txtArrival.getText().trim());
        try {
            if (model.updateRoute(selectedRoute)) {
                clearForm();
                lblMessage.setText("Ruta actualizada.");
            } else {
                lblMessage.setText("Error al actualizar.");
            }
        } catch (Exception e) {
            lblMessage.setText("Error: " + e.getMessage());
        }
    }// GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedRoute == null) {
            lblMessage.setText("Seleccione una ruta para eliminar.");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "¿Eliminar la ruta " + selectedRoute.getId() + "?",
                "Confirmar", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION)
            return;
        try {
            if (model.deleteRoute(selectedRoute.getId())) {
                clearForm();
                lblMessage.setText("Ruta eliminada.");
            } else {
                lblMessage.setText("Error al eliminar.");
            }
        } catch (Exception e) {
            lblMessage.setText("Error: " + e.getMessage());
        }
    }// GEN-LAST:event_btnDeleteActionPerformed

    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnRefreshActionPerformed
        loadRoutesToTable();
        clearForm();
        lblMessage.setText("Lista actualizada.");
    }// GEN-LAST:event_btnRefreshActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAddActionPerformed
        if (!validateForm())
            return;
        Route route = new Route(txtId.getText().trim());
        route.addStation(new Station((String) cmbOrigin.getSelectedItem(), (String) cmbOrigin.getSelectedItem()));
        route.addStation(new Station((String) cmbDest.getSelectedItem(), (String) cmbDest.getSelectedItem()));
        route.setDistance(Double.parseDouble(txtDistance.getText().trim()));
        route.setDepartureTime(txtDeparture.getText().trim());
        route.setArrivalTime(txtArrival.getText().trim());
        try {
            if (model.addRoute(route)) {
                clearForm();
                lblMessage.setText("Ruta agregada.");
            } else {
                lblMessage.setText("Error al agregar ruta.");
            }
        } catch (Exception e) {
            lblMessage.setText("Error: " + e.getMessage());
        }
    }// GEN-LAST:event_btnAddActionPerformed

